    private void txPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txPesquisarActionPerformed
        String texto = txPesquisar.getText();
        modelo.setNumRows(0);
        CategoriaMateriaPrimaControllerNova cmpc = new CategoriaMateriaPrimaControllerNova();
        for (CategoriaMateriaPrima cmp : cmpc.pesquisar(txPesquisar.getText())) {
            modelo.addRow(new Object[]{cmp.getCod(), cmp.getNome(), cmp.getDescricao()});
        }
    }//GEN-LAST:event_txPesquisarActionPerformed
