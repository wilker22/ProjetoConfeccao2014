    /*
     * Método responsável por criar uma tabela.
     * @author Letícia de Jesus Ribeiro
     * @since 1.0 05/11/2013
     * @param 
     * @return
     */
    private void criaJTable() {
        tabela = new JTable(modelo);
        modelo.addColumn("Código");
        modelo.addColumn("Nome");
        modelo.addColumn("Descrição");


        preencherJTable();
    }

    /*
     * Método responsável por preencher a tabela.
     *@author Letícia de Jesus Ribeiro
     * @since 1.0 05/11/2013
     * @param 
     * @return
     */
    private void preencherJTable() {
        CategoriaMateriaPrimaControllerNova cmpc = new CategoriaMateriaPrimaControllerNova();
        for (CategoriaMateriaPrima cmp : cmpc.listAll()) {
            modelo.addRow(new Object[]{cmp.getCod(), cmp.getNome(), cmp.getDescricao()});

        }

    }
