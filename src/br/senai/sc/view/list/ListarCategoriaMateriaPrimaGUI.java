    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int linhaSelecionada = -1;
        linhaSelecionada = tabela.getSelectedRow();
        if (linhaSelecionada >= 0) {
            try {
                int CodCategoriaMateriaPrima = (int) tabela.getValueAt(linhaSelecionada, 0);
                CategoriaMateriaPrimaControllerNova cmpc = new CategoriaMateriaPrimaControllerNova();
                if (cmpc.delete(CodCategoriaMateriaPrima)) {
                    modelo.removeRow(linhaSelecionada);
                }
            } catch (Exception e) {
                System.out.println("Erro ao deletar uma categoria de matéria-prima." + e.getMessage());
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        int linhaSelecionada = -1;
        linhaSelecionada = tabela.getSelectedRow();
        if (linhaSelecionada >= 0) {
            int CodCategoriaMateriaPrima = (int) tabela.getValueAt(linhaSelecionada, 0);
            CategoriaMateriaPrimaControllerNova cmpc = new CategoriaMateriaPrimaControllerNova();
            InserirCategoriaMateriaPrimaGUI icmp = new InserirCategoriaMateriaPrimaGUI(modelo,
                    linhaSelecionada, CodCategoriaMateriaPrima);
            icmp.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "É necessário selecionar uma "
                    + "linha");
        }

    }

