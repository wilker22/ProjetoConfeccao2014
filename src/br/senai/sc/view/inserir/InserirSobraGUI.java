    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

//        if (Validadores.validaCampoVazio(txCategoriaSobra.getText())
//                && Validadores.validaCampoVazio(txCategoriaSobra.getText())
//                && Validadores.validaCampoVazio(txDtCadastroSobra.getText())
//                && Validadores.validaCampoVazio(cbFunResponsavelSobra.getSelectedItem().toString())) {

            Sobra sob = new Sobra();

            //fun.setNome(txNomeFuncionario.getText());
            sob.setCategoria(txCategoriaSobra.getText());
            sob.setMateriaPrima(txMateriaPrimaSobra.getText());
            try {
                String data = txDtCadastroSobra.getText();

                sob.setDtCadastro(new SimpleDateFormat("dd/mm/yyyy").parse(data));
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Data incorreta" + ex);
            }
            sob.setFuncionarioResponsavel((Funcionario) cbFunResponsavelSobra.getSelectedItem());

            SobraController sc = new SobraController();
            if (!(txIdSobra.getText().equals("")
                    || (txIdSobra.getText().equals(null)))) {
                sob.setCodSobra(Integer.parseInt(txIdSobra.getText()));
                sc.update(sob);
                modelo.removeRow(linhaSelecionada);
                modelo.addRow(new Object[]{sob.getCodSobra(), sob.getCategoria(), sob.getMateriaPrima(), sob.getDtCadastro()});
                this.dispose();
            } else {
                int id = sc.inserir(sob);
                modelo.addRow(new Object[]{id, sob.getCategoria(), sob.getMateriaPrima(), sob.getDtCadastro()});
                this.dispose();
            }

    //}//GEN-LAST:event_btSalvarActionPerformed
    }
