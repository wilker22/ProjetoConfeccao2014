    private void carregarComboFuncionario() {
        
        DefaultComboBoxModel comboModel = (DefaultComboBoxModel) cbFunResponsavelSobra.getModel();
        comboModel.removeAllElements();
        List<Funcionario> fun = new ArrayList<>();
        FuncionarioController fc = new FuncionarioController();
        fun = fc.listAll();

        for (int linha = 0; linha < fun.size(); linha++) {
            Funcionario sob1 = fun.get(linha);
            comboModel.addElement(sob1);
        }
    }
