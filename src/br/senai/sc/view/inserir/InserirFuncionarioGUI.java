            Funcionario fun = new Funcionario();


            fun.setNome(txNomeFuncionario.getText());
            fun.setTelefone(txTelefoneFuncionario.getText());
            fun.setEmail(txEmailFuncionario.getText());
            fun.setCpf(txCpfFuncionario.getText());
