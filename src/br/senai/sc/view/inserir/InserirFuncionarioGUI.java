    public InserirFuncionarioGUI(DefaultTableModel modelo, int linhaSelecionada, int idFuncionario) {
        initComponents();

        this.modelo = modelo;
        this.linhaSelecionada = linhaSelecionada;

        FuncionarioController fc = new FuncionarioController();
        Funcionario fun = fc.listById(idFuncionario);
        //Preechemento dos dados do funcionário
        txIdFuncionario.setText(String.valueOf(fun.getCod()));
        txNomeFuncionario.setText(fun.getNome());
        txEmailFuncionario.setText(fun.getEmail());

        SimpleDateFormat formataDataCad = new SimpleDateFormat("dd/MM/yyyy");
        String dataCad = formataDataCad.format(fun.getDtCadastro());
        txDtCadastroFuncionario.setText(dataCad);

        txTelefoneFuncionario.setText(fun.getTelefone());
        txCpfFuncionario.setText(fun.getCpf());
        txRgFuncionario.setText(fun.getRg());
        txEnderecoFuncionario.setText(fun.getEndereco());

        SimpleDateFormat formataDataN = new SimpleDateFormat("dd/MM/yyyy");
        String dataN = formataDataN.format(fun.getDtNascimento());
        txDtNascimentoFuncionario.setText(dataN);

        txLoginFuncionario.setText(fun.getLogin());

        SimpleDateFormat formataDataAd = new SimpleDateFormat("dd/MM/yyyy");
        String dataAd = formataDataAd.format(fun.getDtAdmissao());
        txDtAdmissao.setText(dataAd);

        SimpleDateFormat formataDataAr = new SimpleDateFormat("dd/MM/yyyy");
        String dataAr = formataDataAr.format(fun.getDtRecisao());
        txDtRecisao.setText(dataAr);

        cbCargoFuncionario.setSelectedItem(fun.getCargo());

        txSalarioFuncionario.setText(String.valueOf(fun.getSalario()));
        txCtpsFuncionario.setText(fun.getCtps());





        try {
            String data1 = txDtAdmissao.getText();

            fun.setDtAdmissao(new SimpleDateFormat("dd/mm/yyyy").parse(data1));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Data incorreta" + ex);
        }







        setLocationRelativeTo(null);
    }

